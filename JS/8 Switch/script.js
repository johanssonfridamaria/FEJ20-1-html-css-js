let color = 'pink';

switch(color) {

    case 'red':
        console.log('The color is red');
        break;
    case 'blue':
        console.log('The color is blue');
        break;
    case 'orange':
        console.log('The color is orange');
        break;
    case 'white':
        console.log('The color is white');
        break;
    case 'black':
        console.log('The color is black');
        break;

    default:
        console.log('color unknown!');

}
